def _iso(dt: datetime) -> str:
    return dt.isoformat()


def _safe_tz(tz_name: str):
    try:
        return ZoneInfo(tz_name or "UTC"), (tz_name or "UTC")
    except Exception:
        return timezone.utc, "UTC"


def _parse_hhmm(hhmm: str) -> tuple[int, int]:
    hhmm = (hhmm or "21:00").strip()
    try:
        hh = int(hhmm[:2])
        mm = int(hhmm[3:5])
    except Exception:
        return 21, 0
    if hh < 0 or hh > 23 or mm < 0 or mm > 59:
        return 21, 0
    return hh, mm


def _fmt_money(n: int) -> str:
    s = str(abs(int(n)))
    parts = []
    while s:
        parts.append(s[-3:])
        s = s[:-3]
    return " ".join(reversed(parts))


def _ru_entries_word(n: int) -> str:
    n = abs(int(n)) % 100
    n1 = n % 10
    if 11 <= n <= 14:
        return "записей"
    if n1 == 1:
        return "запись"
    if 2 <= n1 <= 4:
        return "записи"
    return "записей"


def _today_snapshot_prefix(
    lang: str,
    *,
    cnt: int,
    income: int,
    expense_raw: int,
    currency: str,
) -> str:
    """Short header for nudges / pre-report: entry count + optional spend/income (not for morning tone)."""
    lang = (lang or "ru").lower()
    from app.domain.money import fmt_money_compact

    cnt = int(cnt or 0)
    inc_mag = int(income or 0)
    exp_mag = abs(int(expense_raw or 0))

    if lang == "en":
        if cnt == 0:
            line1 = "📋 <b>No entries today yet.</b>"
        elif cnt == 1:
            line1 = "📋 <b>Already 1 entry today.</b>"
        else:
            line1 = f"📋 <b>Already {cnt} entries today.</b>"
    elif lang == "kk":
        if cnt == 0:
            line1 = "📋 <b>Бүгін әлі жазба жоқ.</b>"
        elif cnt == 1:
            line1 = "📋 <b>Бүгін бұрыннан 1 жазба бар.</b>"
        else:
            line1 = f"📋 <b>Бүгін қазірдің өзінде {cnt} жазба.</b>"
    else:
        if cnt == 0:
            line1 = "📋 <b>Сегодня в учёте пока пусто.</b>"
        else:
            line1 = f"📋 <b>Уже {cnt} {_ru_entries_word(cnt)} сегодня.</b>"

    lines: list[str] = [line1]
    if cnt > 0 and (exp_mag > 0 or inc_mag > 0):
        if lang == "en":
            parts = []
            if exp_mag > 0:
                parts.append(f"Spent: −{fmt_money_compact(exp_mag, currency)}")
            if inc_mag > 0:
                parts.append(f"Income: +{fmt_money_compact(inc_mag, currency)}")
            lines.append(" · ".join(parts))
        elif lang == "kk":
            parts = []
            if exp_mag > 0:
                parts.append(f"Шығыс: −{fmt_money_compact(exp_mag, currency)}")
            if inc_mag > 0:
                parts.append(f"Кіріс: +{fmt_money_compact(inc_mag, currency)}")
            lines.append(" · ".join(parts))
        else:
            parts = []
            if exp_mag > 0:
                parts.append(f"Расходы: −{fmt_money_compact(exp_mag, currency)}")
            if inc_mag > 0:
                parts.append(f"Доходы: +{fmt_money_compact(inc_mag, currency)}")
            lines.append(" · ".join(parts))

    lines.append("")
    return "\n".join(lines)


def _day_bounds_utc(now_utc: datetime, tz_name: str) -> tuple[datetime, datetime, str, str]:
    tz, tz_norm = _safe_tz(tz_name)
    local_now = now_utc.astimezone(tz)
    d = local_now.date()
    local_start = datetime(d.year, d.month, d.day, 0, 0, 0, tzinfo=tz)
    local_end = local_start + timedelta(days=1)
    return (
        local_start.astimezone(timezone.utc),
        local_end.astimezone(timezone.utc),
        d.isoformat(),
        tz_norm,
    )


def _in_window(now_local: datetime, target_local: datetime, window: timedelta) -> bool:
    return target_local <= now_local < (target_local + window)


def _pick_variant(options: list[str], user_id: int, local_now: datetime, salt: str) -> str:
    if not options:
        return ''
    idx = (user_id + local_now.toordinal() + local_now.hour + sum(ord(ch) for ch in salt)) % len(options)
    return options[idx]


def _build_nudge_text(user_id: int, local_now: datetime, cnt_today: int) -> str:
    hour = int(local_now.hour)

    if hour < 11:
        pool = MORNING_ZERO if cnt_today <= 0 else MORNING_ACTIVE
        salt = 'morning-zero' if cnt_today <= 0 else 'morning-active'
        return _pick_variant(pool, user_id, local_now, salt)

    if hour < 14:
        pool = DAY_SOFT_ZERO if cnt_today <= 0 else DAY_SOFT_ACTIVE
        salt = 'day-soft-zero' if cnt_today <= 0 else 'day-soft-active'
        return _pick_variant(pool, user_id, local_now, salt)

    if hour < 19:
        pool = DAY_STRICT_ZERO if cnt_today <= 0 else DAY_STRICT_ACTIVE
        salt = 'day-strict-zero' if cnt_today <= 0 else 'day-strict-active'
        return _pick_variant(pool, user_id, local_now, salt)

    pool = EVENING_ZERO if cnt_today <= 0 else EVENING_ACTIVE
    salt = 'evening-zero' if cnt_today <= 0 else 'evening-active'
    return _pick_variant(pool, user_id, local_now, salt)


def _build_pre_text(user_id: int, local_now: datetime, cnt_today: int) -> str:
    if cnt_today <= 0:
        return _pick_variant(PRE_ZERO, user_id, local_now, 'pre-zero')
    if cnt_today <= 2:
        return _pick_variant(PRE_LOW, user_id, local_now, 'pre-low').format(cnt=cnt_today)
    if cnt_today <= 5:
        return _pick_variant(PRE_MID, user_id, local_now, 'pre-mid').format(cnt=cnt_today)
    return _pick_variant(PRE_HIGH, user_id, local_now, 'pre-high').format(cnt=cnt_today)


async def _send_safe(bot, user_id: int, text: str, parse_mode: str | None = None):
    try:
        kwargs = {"parse_mode": parse_mode} if parse_mode else {}
        await bot.send_message(user_id, text, **kwargs)
    except Exception as e:
        err_msg = str(e).lower()
        # Mute common "user left" errors to INFO level instead of WARNING
        if "chat not found" in err_msg or "forbidden" in err_msg or "user is deactivated" in err_msg:
            logger.info(f"skipping inactive user {user_id}: {e}")
        else:
            logger.warning(f"send failed uid={user_id}: {e}")


