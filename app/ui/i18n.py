def _strip_leading_emoji_symbols(text: str) -> str:
    """Strip decorative emoji/pictographs often used as the first grapheme(s)
    on reply-keyboard labels (e.g. ``✅ Доход`` vs a stale ``Доход`` key)."""
    s = (text or "").strip()
    if not s:
        return s
    i = 0
    n = len(s)
    while i < n:
        c = s[i]
        if c.isspace() or c in "\ufe0f\u200d":
            i += 1
            continue
        o = ord(c)
        if (
            0x2600 <= o <= 0x26FF
            or 0x2700 <= o <= 0x27BF
            or 0x1F300 <= o <= 0x1FAFF
            or 0x1F600 <= o <= 0x1F64F
            or 0x1F680 <= o <= 0x1F6FF
            or 0x1F1E0 <= o <= 0x1F1FF
        ):
            i += 1
            continue
        break
    return s[i:].strip()


