ALL_LANGS = ("ru", "en", "kk")

def t(lang: str, key: str) -> str:
    lang = (lang or "ru").lower()
    return TEXT.get(lang, TEXT["ru"]).get(key, TEXT["ru"].get(key, key))

def text_matches_key(text: str | None, key: str) -> bool:
    raw = (text or "").strip().casefold()
    return any(t(lang, key).strip().casefold() == raw for lang in ALL_LANGS)

def any_labels(key: str) -> set[str]:
    return {t(lang, key) for lang in ALL_LANGS}
